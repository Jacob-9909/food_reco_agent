def search_restaurants(location, cuisine, weather):
    """
    주어진 위치, 음식 종류, 날씨에 맞는 맛집을 검색합니다.
    
    Args:
        location (str): 위치 (예: '서울', '부산', '대구')
        cuisine (str): 음식 종류 (예: '한식', '중식', '일식', '양식')
        weather (str): 날씨 (예: '맑음', '흐림', '비', '더움', '추움')
        
    Returns:
        list: 맛집 추천 목록
    """
    # 위치, 음식 종류, 날씨에 따른 맛집 검색
    if location in restaurant_data:
        if cuisine in restaurant_data[location]:
            if weather in restaurant_data[location][cuisine]:
                return restaurant_data[location][cuisine][weather]
    
    # 일치하는 데이터가 없을 경우 서울 한식 맑음 날씨 기본 추천
    return restaurant_data["서울"]["한식"]["맑음"]
